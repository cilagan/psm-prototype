package gov.nsf.research.psm.model;

/**
 * ProgramElement
 * 
 * @author spendyal
 *
 */

public class ProgramElement {
	
	private String divisionID;
	
	private String programElementCode;
	
	private String programElmentCodeDesc;

	public String getDivisionID() {
		return divisionID;
	}

	public void setDivisionID(String divisionID) {
		this.divisionID = divisionID;
	}

	public String getProgramElementCode() {
		return programElementCode;
	}

	public void setProgramElementCode(String programElementCode) {
		this.programElementCode = programElementCode;
	}

	public String getProgramElmentCodeDesc() {
		return programElmentCodeDesc;
	}

	public void setProgramElmentCodeDesc(String programElmentCodeDesc) {
		this.programElmentCodeDesc = programElmentCodeDesc;
	}
	

}
